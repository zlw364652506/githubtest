package com.aj.frame.ydjwpt;

/** 
 * <b>消息通知业务类型定义</b>
 * @author liuling 
 *  
 */
public class NoticeInfoRecordBusType {
	
	/**
	 * 默认系统非业务类通知
	 */
	public final static byte Default = 0;
	
	/**
	 * APP意见反馈
	 */
	public final static byte AppFeedbackOpinion = 1;
	
	/**
	 * APP意见反馈回复
	 */
	public final static byte AppOpinionReply = 2;
	
	/**
	 * 开发者账户申请待审核
	 */
	public final static byte DevlopAccountReviewing = 3;

	/**
	 * 新发APP申请待审核 (4)
	 */
	public final static byte AppApplyReviewing = 4;
	
	/**
	 * 更新APP申请待审核(5)
	 */
	public final static byte AppReapplyReviewing = 5;
	
	/**
	 * 新发app申请评测
	 */
	public final static byte AppApplyEvaluating = 6;
	
	/**
	 * 更新app申请评测
	 */
	public final static byte AppReapplyEvaluating = 7;
	
	/**
	 * 开发者帐号审核通过
	 */
	public final static byte DevlopAccountReviewedPass = 8;
	
	/**
	 * 新发app申请审核通过
	 */
	public final static byte AppApplyReviewedPass = 9;
	
	/**
	 * 新发app申请审核未通过
	 */
	public final static byte AppApplyReviewedNotPass = 10;
	
	/**
	 * 更新app申请审核通过
	 */
	public final static byte AppReapplyReviewedPass = 11;
	
	/**
	 * 更新app申请审核未通过
	 */
	public final static byte AppReapplyReviewedNotPass = 12;

	/**
	 * 新发/更新app申请评测未通过
	 */
	public final static byte AppApplyEvaluatedNotPass = 13;
	
	/**
	 * 调用的基础服务被停用
	 */
	public final static byte ProvideBaseServicesDisabled = 14;
	
	/**
	 * 平台管理员下架APP
	 */
	public final static byte AppWasDown = 15;
	
	/**
	 * 平台管理员撤回版本
	 */
	public final static byte AppversionRevoked = 16;
	
	/**
	 * 用户设备待授权
	 */
	public final static byte DeviceAuthing = 17;
	
	/**
	 * 终端共享用户待授权
	 */
	public final static byte DeviceShareAuthing = 18;
	
}
