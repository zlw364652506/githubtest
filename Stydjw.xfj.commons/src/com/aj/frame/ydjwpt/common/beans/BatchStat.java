package com.aj.frame.ydjwpt.common.beans;
import java.util.Date;

import com.aj.frame.beans.AJFrameNamedBeanAbstract;

/**
 * 发机批次统计
 * @author chenzhirong
 *
 */
public class BatchStat extends AJFrameNamedBeanAbstract {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4757187167485940068L;
    /**
     * 主键
     */
	private Long objId; 
	/**
	 * 批次主键
	 */
	private Long batchObjId;
	/**
	 * 已授权数
	 */
	private Long authCount;
	/**
	 * 待授权数
	 */
	private Long waitAuthCount;
	/**
	 * 已匹配数
	 */
	private Long matchCount;
	/**
	 * 未匹配数
	 */
	private Long notMatchCount;
	/**
	 * 统计时间
	 */
	private Date statTime;
	/**
	 * 发机批次
	 */
	private Batch batch;
	
	public Batch getBatch() {
		return batch;
	}


	public void setBatch(Batch batch) {
		this.batch = batch;
	}


	public Long getObjId() {
		return objId;
	}


	public void setObjId(Long objId) {
		this.objId = objId;
	}


	public Long getBatchObjId() {
		return batchObjId;
	}


	public void setBatchObjId(Long batchObjId) {
		this.batchObjId = batchObjId;
	}


	public Long getAuthCount() {
		return authCount;
	}


	public void setAuthCount(Long authCount) {
		this.authCount = authCount;
	}


	public Long getWaitAuthCount() {
		return waitAuthCount;
	}


	public void setWaitAuthCount(Long waitAuthCount) {
		this.waitAuthCount = waitAuthCount;
	}


	public Long getMatchCount() {
		return matchCount;
	}


	public void setMatchCount(Long matchCount) {
		this.matchCount = matchCount;
	}


	public Long getNotMatchCount() {
		return notMatchCount;
	}


	public void setNotMatchCount(Long notMatchCount) {
		this.notMatchCount = notMatchCount;
	}


	public Date getStatTime() {
		return statTime;
	}


	public void setStatTime(Date statTime) {
		this.statTime = statTime;
	}


	public static long getSerialversionuid() {
		return serialVersionUID;
	}

	
	@Override
	public String[] importantFieldValues() {
		return null;
	}
	
}
